class TwoDiagonals {
    public:
    int maxPoints(vector<int> x, vector<int> y) {
        map<int, vector<int> > mp1, mp2;
        int n = (int)x.size();

        rep(n){
            mp1[x[i]-y[i]].push_back(i);
            mp2[x[i]+y[i]].push_back(i);
        }
        
        int ret = -1;
        for(auto itr1=mp1.begin();itr1!=mp1.end();itr1++){
            for(auto itr2=mp2.begin();itr2!=mp2.end();itr2++){
                vector<int> v;
                set_union((itr1->second).begin(), (itr1->second).end(), (itr2->second).begin(), (itr2->second).end(), back_inserter(v));
                ret = max(ret, (int)v.size());
            }
        }
        return ret;
        return 0;
    }
};

